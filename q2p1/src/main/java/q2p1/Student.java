package q2p1;

public class Student {
	private Double tuition;
	private Double apr;
	private Double time_to_pay;
	private Double cost_of_college;
	private Double perc_increase;
	private int years_in_school;
	
	public Double getTuition() {
		return tuition;
	}
	public void setTuition(Double tuition) {
		this.tuition = tuition;
	}	
	public Double getApr() {
		return apr;
	}
	public void setApr(Double apr) {
		this.apr = apr;
	}
	public Double getTime_to_pay() {
		return time_to_pay;
	}
	public void setTime_to_pay(Double time_to_pay) {
		this.time_to_pay = time_to_pay;
	}
	public Double getCost_of_college() {
		return cost_of_college;
	}
	public void setCost_of_college(Double cost_of_college) {
		this.cost_of_college = cost_of_college;
	}
	public Double getPerc_increase() {
		return perc_increase;
	}
	public void setPerc_increase(Double perc_increase) {
		this.perc_increase = perc_increase;
	}
	public int getYears_in_school() {
		return years_in_school;
	}
	public void setYears_in_school(int years_in_school) {
		this.years_in_school = years_in_school;
	}

}
